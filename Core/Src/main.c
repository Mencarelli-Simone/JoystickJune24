/**
  * @brief  The application entry point.
  * @retval int
  */
int main(void)
{

  /* USER CODE BEGIN 1 */
  // HID Mouse
  typedef struct joyHID_t {
	  int16_t X;
	  int16_t Y;
	  int16_t Z;
	  int16_t Rx;
	  int16_t Ry;
	  int16_t Rz;
  }joyHID_t;
  joyHID_t mouseHID={
				 X: 0,
				 Y: 0,
				 Rx: 0,
				 Ry: 0};
  /* USER CODE END 1 */

  /* MCU Configuration--------------------------------------------------------*/

  /* Reset of all peripherals, Initializes the Flash interface and the Systick. */
  HAL_Init();

  /* USER CODE BEGIN Init */

  /* USER CODE END Init */

  /* Configure the system clock */
  SystemClock_Config();

  /* USER CODE BEGIN SysInit */

  /* USER CODE END SysInit */

  /* Initialize all configured peripherals */
  MX_GPIO_Init();
  MX_DMA_Init();
  MX_USB_DEVICE_Init();
  MX_ADC1_Init();
  MX_TIM3_Init();
  /* USER CODE BEGIN 2 */

  // Start timer1 (to time adc conversions)
  HAL_TIM_Base_Start_IT(&htim3);
  // Start the DMA
  HAL_ADC_Start_DMA(&hadc1, (uint32_t *) adc_results, sizeof(adc_results)/sizeof(adc_results[0]));
  // USB handle
  USBD_HandleTypeDef *pdev =  MX_USB_DEVICE_getHandle(); // get the handler for hid joystick device
  /* USER CODE END 2 */

  /* Infinite loop */
  /* USER CODE BEGIN WHILE */
  // buffer index
  uint16_t i = 0;
  while (1)
  {
    /* USER CODE END WHILE */

    /* USER CODE BEGIN 3 */

	  // wait for the adc to complete the acquisition
	  while(adc_complete == 0){}
	  // reset adc complete
	  adc_complete = 0;
	  // fill buffer
	  if (i > (ADCBUFF / 4))
	  {
		  i = 0;
	  }
	  adc_buffer[i * 4] = adc_results[0];
	  adc_buffer[i * 4 + 1] = adc_results[1];
	  adc_buffer[i * 4 + 2] = adc_results[2];
	  adc_buffer[i * 4 + 3] = adc_results[3];
	  // increase i
	  i++;
	  // perform average for each channel
	  adc_average[0] = 0; // reset average
	  adc_average[1] = 0;
	  adc_average[2] = 0;
	  adc_average[3] = 0;
	  for(int j = 0; j < ADCBUFF / 4; j++)
	  {
		  adc_average[0] += adc_buffer[j * 4] / (ADCBUFF / 4);
		  adc_average[1] += adc_buffer[j * 4 + 1] / (ADCBUFF / 4);
		  adc_average[2] += adc_buffer[j * 4 + 2] / (ADCBUFF / 4);
		  adc_average[3] += adc_buffer[j * 4 + 3] / (ADCBUFF / 4);
	  }
	  // SEND USB REPORT
	  mouseHID.X =	(int16_t) ((adc_average[0]) / 8); // for 8 bit output positive only
	  mouseHID.Y =	(int16_t) ((adc_average[1]) / 8);
	  mouseHID.Rx =	(int16_t) ((adc_average[2]) / 8);
	  mouseHID.Ry =	(int16_t) ((adc_average[3]) / 8);
	  USBD_HID_SendReport(pdev, &mouseHID, sizeof(joyHID_t));

  }
  /* USER CODE END 3 */
}

